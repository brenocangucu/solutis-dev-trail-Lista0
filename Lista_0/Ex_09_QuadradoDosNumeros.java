package Lista_0;

//Encontre o quadrado dos números de 0 até 10. Utilize o controle de fluxo for.

public class Ex_09_QuadradoDosNumeros {

    public static void main(String[] args){

        for(int i = 0 ; i <= 10; i++){
            System.out.println("Quadrado de " + i + " é: " + i * i);
        }
    }
}
